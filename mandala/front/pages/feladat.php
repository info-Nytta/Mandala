	<h3>FELADAT</h3>
	<ol>
		<li>
			<h4>Statikus tartalmak</h4>
			<ul>
				<li>
				<span class='kiemelt'>A fejléc</span> a minta szerinti elrendezésben és tartalommal jelenjen meg! Az oldal a minta szerinti kezdőlappal induljon!
				</li>
				<li>
				<span class='kiemelt'>A menü</span> négy menüpontból áll: <strong><i>"Feladatleírás", "Mandala", "Galéria", "Kedvencek". </i></strong> A <strong><i>"Feladatleírás"</i></strong> menüpont egy külső link legyen, amely pontosan erre a leírásra mutasson! 
				</li>
				<li>
				<span class='kiemelt'>A fő tartalmi rész</span> a menüválasztástól függ. Az egyetlen statikus tartalom a kezdőlap, ami az oldal indulásakor vagy a "Mandala" menüpont választásakor jelenik meg.
				</li>
				<li>
				<span class='kiemelt'>A lábléc</span> az oldal készítőjének nevét vagy monogramját, esetleg az aktuális évet és hónapot tartalmazza!
				</li>
				<li>
				Mobil nézetben az oldal jobb alsó sarkában jelenjen meg egy <span class='kiemelt'>felfelé mutató hivatkozás</span> szabadon választott karakterrel, amellyel az oldal tetejére tudunk ugrani!
				</li>
			</ul>
		</li>
		<li>
			<h4>Menükezelés</h4>
			<ul>
				<li>
					A menü kezelését <span class='kiemelt'>paraméterezéssel</span> oldja meg úgy, hogy a paraméterek értékeiből ne lehessen következtetni fájlnevekre!
				</li>
				<li>
					Abban az esetben, ha olyan érték került megadásra, amelyhez nem tartozik egyik menüpont sem, akkor a kezdőoldal töltődjön be!
				</li>
			</ul>
		</li>
		<li>
			<h4>Galéria</h4>
			<ul>
				<li>
					Jelenítse meg a mandalákat tartalmazó mappában található összes képet úgy, hogy mindegyik kép hivatkozás, amely külön ablakban megjeleníti az adott képet!
				</li>
				<li>
					Minden kép alatt jelenjen meg egy <strong><i>"Kedvencekhez"</i></strong> hivatkozás!
				</li>
				<li>
					Ha a felhasználó rákkattint a <strong><i>"Kedvencekhez"</i></strong> hivatkozásra, akkor a minta szerint jelenjen meg a szöveg, a kiválasztott kép és alatta egy hivatkozás, amivel vissza tud menni a galériába!
				</li>
					Oldja meg, hogy amelyik képet a felhasználó kiválasztotta, azt ne választhassa újra!
				</li>
				<li>
					Oldja meg, hogy ha a felhasználó elkattint másik menüpontra, de utána ide visszatér, akkor a korábban kiválasztott képeit már ne választhassa újra!
				</li>
			</ul>
		</li>
		<li>
			<h4>Kedvencek</h4>
			<ul>
				<li>
					Oldja meg, hogy a felhasználó eddig kiválasztott képei jelenjenek meg ugyanabban a stílusban, mint a galériában! Ha még nem választott kedvencet, akkor minta szerint egy üzenet tájékoztassa erre.
				</li>
				<li>
					Minden kép alatt legyen egy <strong><i>"Mégsem kedvenc"</i></strong> hivatkozás, amellyel törölheti az adott képet a kedvencekből.
				</li>
			</ul>
		</li>
	</ol>
